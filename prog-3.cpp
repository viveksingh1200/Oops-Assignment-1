#include<iostream>
using namespace std;

int main(){
    int a,b;
    cout<<"Enter a and b: ";
    cin>>a>>b;

    cout<<"Addition is: "<<a+b<<endl;
    cout<<"Subtraction is: "<<a-b<<endl;
    cout<<"Multiplication is: "<<a*b<<endl;
    cout<<"Division is is: "<<a/b<<endl;
    cout<<"Remainder is: "<<a%b<<endl;
    return 0;
}