#include<iostream>
using namespace std;

int main(){
    int a=4;
    cout<<"Uses of increment and decrement operator:"<<endl;
    cout<<"Increment operator:"<<endl;
    cout<<a++<<endl;
    cout<<++a<<endl;

    cout<<"Decrement operator:"<<endl;
    cout<<--a<<endl;
    cout<<a--<<endl;
    return 0;
}